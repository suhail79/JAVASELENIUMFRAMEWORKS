package org.test.ACLearnjavaprograms;

public class Aaclearnswap2numbers {
    public static void main(String[] args) {

        int a = 25;

        int b = 30;

        System.out.println("Before swapping numbers a value is " + a);
        System.out.println("Before swapping numbers b value is " + b);

        int c = a;

        a = b;
        b = c;

        System.out.println("After swapping numbers a value is " + a);
        System.out.println("After swapping numbers b value is " + b);


    }
}
