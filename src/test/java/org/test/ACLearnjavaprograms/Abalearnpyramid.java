package org.test.ACLearnjavaprograms;

public class Abalearnpyramid {
    public static void main(String[] args) {

        // 13. pyramid

        for (int i = 1; i <= 5; i++) {
            for (int j = 0; j < i; j++) {
                System.out.print("*");
            }
            System.out.println();
        }


    }
}