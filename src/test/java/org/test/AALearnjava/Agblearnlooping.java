package org.test.AALearnjava;

public class Agblearnlooping {
    public static void main(String[] args) {

        //For Loop
        for (int i = 0; i < 2; i++) {
            System.out.println(i);
        }

        //While Loop
        int j = 0;
        while (j < 2) {
            System.out.println("while loop");
            j++;
        }
        //Do while
        int k = 0;
        do {
            System.out.println("Do while");
            k++;
        } while (k < 2);


    }

}
